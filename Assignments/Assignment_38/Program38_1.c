//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
 char arr[230] = "Marvellous Python";
 char brr[30];

 StrCpyRev(arr,brr);

 printf("%s",brr);

 return 0;

}//End of main

