void StrCpyRev(char *src, char *dest)
{
  char *start = src;
  char *end = src;
  
  while(*end != '\0')
  {
    end++;
  }
  end--;

  while(end >= start)
  {
    *dest = *end;
    dest++;
    end--;
  }
  *dest = '\0';
 
}

