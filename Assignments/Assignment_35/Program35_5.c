void Reverse(char *str)
{
    char *start = str;
    char *end = str;
    char temp;

    while (*end != '\0')
    {
        end++;
    }
    end--;

    while (start < end)
    {
        temp = *start;
        *start = *end;
        *end = temp;

        start++;
        end--;
    }
} // End of function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////
int main()
{
    char Arr[20];

    printf("Enter the String :\n");
    scanf("%[^'\n]s", Arr);

    Reverse(Arr);

    printf("Reverse String : %s\n", Arr);

    return 0;
} // End of main

