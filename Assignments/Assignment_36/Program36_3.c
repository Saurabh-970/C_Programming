void strtogglex(char str[])
{
    int i = 0;

    while (str[i] != '\0')
    {
        if (i == 0 || str[i - 1] == ' ')
        {
            str[i] = tolower(str[i]);
        }
        else
        {
            str[i] = toupper(str[i]);
        }
        i++;
    }

} // End of function

