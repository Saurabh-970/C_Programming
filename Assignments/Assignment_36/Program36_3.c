//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////
int main()
{
    char Arr[20];

    printf("Enter the String :\n");
    scanf("%[^'\n]s", Arr);

    strtogglex(Arr);

    printf("Modified String is : %s", Arr);

    return 0;
} // End of main

