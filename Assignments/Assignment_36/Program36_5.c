int CountWhite(char str[])
{
    int iCnt = 0;
    int iCount = 0;

    while (str[iCnt] != '\0')
    {
        if (str[iCnt] == ' ')
        {
            iCount++;
        }
        iCnt++;
    }

    return iCount;

} // End of function

