void struprx(char *str)
{
    while (*str != '\0')
    {
        if ((*str >= 'a') && (*str <= 'z'))
        {
            *str = *str - 32;
        }
        str++;
    }

} // End of function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////
int main()
{
    char Arr[20];

    printf("Enter the String :\n");
    scanf("%[^'\n]s", Arr);

    struprx(Arr);

    printf("Modified String is : %s", Arr);

    return 0;
} // End of main

