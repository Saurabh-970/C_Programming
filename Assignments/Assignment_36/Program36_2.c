////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : struprx
//  Description   : Accept String from user & convert it into upper case.
//  Input         : Character
//  Output        : Character
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 2/12/2025
