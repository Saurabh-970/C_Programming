BOOL CheckAlpha(char ch)
{
  if((ch >= 'A') && (ch <= 'Z') || (ch >= 'a') && (ch <= 'z'))
  {
      return TRUE;
  }
  else
  {
    return FALSE;
  }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
  char cVAlue ='\0';
  BOOL bRet = FALSE;

  printf("Enter the character :");
  scanf("%c",&cVAlue);

  bRet = CheckAlpha(cVAlue);

  if(bRet == TRUE)
  {
    printf("It is a character :");
  }
  else
  {
    printf("It is not a character :");
  }

  return 0;

}//End of main

