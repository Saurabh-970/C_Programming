#include <stdio.h>

typedef unsigned int UINT;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ToggleBit
//  Description   : Write a program which accept one number user and toggle contents
//                  of first and last nibble of the number.Return Modified number.
//                  (nibble is a group of four bits)
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 05/02/2026
//
////////////////////////////////////////////////////////////////////////////////

UINT ToggleBit(UINT NO)
{
    UINT Mask = 0;

    Mask = 0xF000000F;

    return (NO ^ Mask);
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINT No = 0;
    UINT Ret = 0;

    printf("Enter the number : \n");
    scanf("%u", &No);

    Ret = ToggleBit(No);

    printf("Modified number is : %u\n", Ret);

    return 0;
}

