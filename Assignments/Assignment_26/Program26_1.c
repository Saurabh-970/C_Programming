#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : Pattern
//  Description   : Accept number from user and display it into Alphabet
//  Input         : Integer
//  Output        : Character
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 20 /11/2025
//
///////////////////////////////////////////////////////////////////////////////////////////


void Pattern(int iNo)                                                                                           
{
    int iCnt = 0;
    char ch = 64;

    for(iCnt =1; iCnt<= iNo; iCnt++)
    {
        ch =(char)(iCnt +64);
        printf("%c\t",ch);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter number of elements");
    scanf("%d",&iValue);

    Pattern(iValue);

    return 0;
}

