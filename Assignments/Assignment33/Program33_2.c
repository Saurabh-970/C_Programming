BOOL CheckCapital(char ch)
{
  if((ch >= 'A') && (ch <= 'Z'))
  {
      return TRUE;
  }
  else
  {
    return FALSE;
  }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
  char cVAlue ='\0';
  BOOL bRet = FALSE;

  printf("Enter the character :");
  scanf("%c",&cVAlue);

  bRet = CheckCapital(cVAlue);

  if(bRet == TRUE)
  {
    printf("It is a capital character :");
  }
  else
  {
    printf("It is not a capital character :");
  }

  return 0;

}//End of main

