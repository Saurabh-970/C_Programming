#include<stdio.h>
#define TRUE 1
#define FALSE 0
typedef int BOOL;

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : CheckAplha
//  Description   : Accept character and chech whether it is capital or not
//  Input         : Character
//  Output        : Character
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 20 /11/2025
//
