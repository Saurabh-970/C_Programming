void Display(char ch)
{
  
  if(ch < 'a'  )
  {
    return;
  }
  Display(ch - 1);
  printf("%c\t",ch + 96);
  
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
  int iValue = 0;

  printf("Enter character :");
  scanf("%d",&iValue);
  Display(iValue);

  return 0;

}//End of main

