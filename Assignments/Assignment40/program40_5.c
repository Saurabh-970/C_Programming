///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : Display
//  Description   : Write a recursive program which display below pattern
//                  5       4       3       2       1
//  Input         : Integer
//  Output        : Character
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 10/12/2025
//
