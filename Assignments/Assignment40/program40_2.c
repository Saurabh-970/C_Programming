void Display(int iNo)
{
  
  if(iNo == 0)
  {
    return;
  }

  Display(iNo - 1);
  printf("%d\t",iNo);

}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
  int iValue = 0;

  printf("Enter number :");
  scanf("%d",&iValue);
  Display(iValue);

  return 0;

}//End of main

