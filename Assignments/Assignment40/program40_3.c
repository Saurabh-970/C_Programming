#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : Display
//  Description   : Write a recursive program which display below pattern
//                  5       4       3       2       1
//  Input         : Integer
//  Output        : Integer
//  Author        : Saurabh Ravindra Bhonsle
//  Date          : 10/12/2025
//
///////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
  
  if(iNo == 0)
  {
    return;
  }
  printf("%d\t",iNo);
  Display(iNo - 1);
  

}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
  int iValue = 0;

  printf("Enter number :");
  scanf("%d",&iValue);
  Display(iValue);

  return 0;

}//End of main

