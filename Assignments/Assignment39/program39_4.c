void Display(char ch)
{
  
  if(ch < 'A')
  {
    return;
  }

  Display(ch - 1);

  printf("%C\t",ch);

}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
  Display('F');

  return 0;

}//End of main

