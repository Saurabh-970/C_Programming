{
 char arr[20];
 char cVAlue;
 int iRet = 0;

  printf("Enter the string :");
  scanf("%[^'\n]s",arr);

  printf("Enter the character you want to find\n");
  scanf(" %c",&cVAlue);

  iRet = CountChar(arr,cVAlue);

  printf("Character frequency is %d",iRet);

  return 0;

}//End of main

