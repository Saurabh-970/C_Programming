int Mult(int iNo)
{

  if(iNo == 0 )
  {
    return 1;
  }
  
  return ((iNo % 10) * Mult(iNo /10));
  
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
  int iValue = 0,iRet = 0;
 

  printf("Enter number :");
  scanf("%d",&iValue);

  iRet = Mult(iValue);

  printf("%d",iRet);

  return 0;

}//End of main

