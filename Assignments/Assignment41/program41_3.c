int Strlen(char *str)
{

  if(*str == '\0' )
  {
    return 0;
  }
  
  return (1 + Strlen (str + 1));
  
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
  int iRet = 0;
  char arr[20];

  printf("Enter String :");
  scanf("%s",arr);

  iRet = Strlen(arr);

  printf("%d",iRet);

  return 0;

}//End of main

